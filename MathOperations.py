# Mathematical operations operate element wise.
# Not on the array itself like regular python lists.

import numpy as np
a = np.array([1,2,3])

# Adds 4 to all elements in the array.
a = a + np.array([4])

# Multiplies all elements in the array by 2.
a = a * 2

# Square roots the array.
a = np.sqrt(a)

